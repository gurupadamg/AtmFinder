package com.atmfinder.exception;

public class AtmDetailsConversionException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AtmDetailsConversionException() {
		super();
	}

	public AtmDetailsConversionException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AtmDetailsConversionException(String message, Throwable cause) {
		super(message, cause);
	}

	public AtmDetailsConversionException(String message) {
		super(message);
	}

	public AtmDetailsConversionException(Throwable cause) {
		super(cause);
	}
	
	

}
